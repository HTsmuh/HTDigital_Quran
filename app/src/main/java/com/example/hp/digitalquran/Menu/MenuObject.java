public class MenuObject {

    private int size;
    private String mode;
    private String script;

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public String getMode() {
        return mode;
    }

    public void setMode(String mode) {
        this.mode = mode;
    }

    public String getScript() {
        return script;
    }

    void setScript(String script) {
        this.script = script;
    }

}
